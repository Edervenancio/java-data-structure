package BigO;

public class BigOConstant {


    // O(1)
    // Para cada entrada, seu tempo de execução será constante. - O(1)
    // Não importa se N for 1, ou 1000000000, ele só irá executar uma operação, tendo um tempo constante.

    public static int multiplyNumbers(int n){
        return n*n;
    }




}
